package com.cyou.library.xh.user.domain;

import java.io.Serializable;

/**
 * @author Administrator
 *
 */
public class DivisionPower implements Serializable{

	private static final long serialVersionUID = 1L;

	private int divisionId;
	private int roleId;
	private int funId;
	
	public DivisionPower() {
	}

	public int getDivisionId() {
		return divisionId;
	}

	public void setDivisionId(int divisionId) {
		this.divisionId = divisionId;
	}

	public int getRoleId() {
		return roleId;
	}

	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}

	public int getFunId() {
		return funId;
	}

	public void setFunId(int funId) {
		this.funId = funId;
	}

	public String toString() {
		return "DivisionPower [divisionId=" + divisionId + ", roleId=" + roleId
				+ ", funId=" + funId + "]";
	}

	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + divisionId;
		result = prime * result + funId;
		result = prime * result + roleId;
		return result;
	}

	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DivisionPower other = (DivisionPower) obj;
		if (funId != other.funId)
			return false;
		if (roleId != other.roleId)
			return false;
		return true;
	}
	
	
	
	
}
